        PNCUDMIS.DATA.APP_NM = PNB2600_AWA_2600.APP_NM;
        PNCUDMIS.DATA.APP_ADDR = PNB2600_AWA_2600.APP_ADDR;
        PNCUDMIS.DATA.APP_TEL = PNB2600_AWA_2600.APP_TEL;
        PNCUDMIS.DATA.ID_TYPE = PNB2600_AWA_2600.ID_TYPE;
