        PNCUCCLR.LHD_NM = PNB1200_AWA_1200.LHD_NM;
        CEP.TRC(SCCGWA, PNB1200_AWA_1200.LHD_NM);
        PNCUCCLR.CLR_BK = PNB1200_AWA_1200.CLR_BK;
        CEP.TRC(SCCGWA, PNB1200_AWA_1200.CLR_BK);
        PNCUCCLR.PRDMO_CD = PNB1200_AWA_1200.PRDMO_CD;
        CEP.TRC(SCCGWA, PNB1200_AWA_1200.PRDMO_CD);
